# ══════════════════════════════════════════════════════════
#  CUSTOMIZE: Task list — format: "task_id|phase|description"
#  Must match PRD.md checkboxes exactly.
# ══════════════════════════════════════════════════════════
TASKS=(
  # "1.1|1|First task"
  # "1.2|1|Second task"
  # "2.1|2|Third task"
)

# ══════════════════════════════════════════════════════════
#  CUSTOMIZE: Phase names (for commit messages and logging)
# ══════════════════════════════════════════════════════════
get_phase_name() {
  case "$1" in
    1) echo "Phase 1 Name" ;;
    2) echo "Phase 2 Name" ;;
    # Add more phases...
  esac
}

# ── Helpers ───────────────────────────────────────────────
timestamp() { date "+%Y-%m-%d %H:%M:%S" }

log() {
  echo -e "${CYAN}[$(timestamp)]${NC} $1" | tee -a "$LOG_FILE"
}

log_success() {
  echo -e "${GREEN}[$(timestamp)] ✓${NC} $1" | tee -a "$LOG_FILE"
}

log_error() {
  echo -e "${RED}[$(timestamp)] ✗${NC} $1" | tee -a "$LOG_FILE"
}

log_phase() {
  echo "" | tee -a "$LOG_FILE"
  echo -e "${BOLD}${BLUE}━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━${NC}" | tee -a "$LOG_FILE"
  echo -e "${BOLD}${BLUE}  Phase $1${NC}" | tee -a "$LOG_FILE"
  echo -e "${BOLD}${BLUE}━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━${NC}" | tee -a "$LOG_FILE"
}

# Check if a task is already marked done in PRD.md
is_task_done() {
  local task_id="$1"
  grep -q "\[x\] \*\*${task_id}\*\*" "$PRD_FILE" 2>/dev/null
}

# Mark a task as done in PRD.md ([ ] → [x])
# NOTE: macOS sed syntax. On Linux, change `sed -i ''` to `sed -i`
mark_task_done() {
  local task_id="$1"
  sed -i '' "s/- \[ \] \*\*${task_id}\*\*/- [x] **${task_id}**/" "$PRD_FILE"
}

# ── Init findings file ────────────────────────────────────
init_findings() {
  if [[ ! -f "$FINDINGS_FILE" ]]; then
    cat > "$FINDINGS_FILE" << 'EOF'
# Findings & Decisions Log

Auto-generated by ralph. Each task logs its decisions and findings here.

---

EOF
    log "Created FINDINGS.md"
  fi
}

