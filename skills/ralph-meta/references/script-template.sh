# ── Fix build errors via Claude ───────────────────────────
fix_build() {
  local phase="$1"
  local error_output="$2"
  local attempt="$3"

  log "  Fix attempt ${attempt}/${MAX_FIX_ATTEMPTS}..."

  local prompt="You are implementing a software project.

PROJECT DIRECTORY: $PROJECT_DIR

The build/validation failed after completing Phase ${phase}. Fix the errors below.

ERROR OUTPUT:
\`\`\`
${error_output}
\`\`\`

RULES:
- Read the relevant files to understand the context before fixing.
- Only fix what's broken. Do not refactor or change unrelated code.
- Read PRD.md if you need to understand the intended design.
- After fixing, append a brief note to FINDINGS.md under '## Phase ${phase} — Build Fix (attempt ${attempt})' describing what went wrong and how you fixed it.
- Do NOT commit anything to git.
- Do NOT run the dev server."

  claude -p "$prompt" \
    --model sonnet \
    --allowedTools "Bash,Read,Write,Edit,Glob,Grep" \
    >> "$LOG_FILE" 2>&1 || true
}

# ── Commit a phase ────────────────────────────────────────
commit_phase() {
  local phase="$1"
  local phase_name="$2"

  log "  Committing Phase ${phase}..."
  cd "$PROJECT_DIR"
  git add -A
  git commit -m "$(cat <<EOF
phase ${phase}: ${phase_name}

Automated by ralph.sh
EOF
)" 2>&1 | tee -a "$LOG_FILE"
  log_success "  Phase ${phase} committed"
}

# ── Main loop ─────────────────────────────────────────────
main() {
  echo "" > "$LOG_FILE"
  log "${BOLD}Ralph — automated builder${NC}"
  log "Project: $PROJECT_DIR"
  log ""

  init_findings

  local current_phase=0

  for task_entry in "${TASKS[@]}"; do
    local task_id="${task_entry%%|*}"
    local remainder="${task_entry#*|}"
    local phase="${remainder%%|*}"
    local description="${remainder#*|}"

    # ── Phase boundary: sanity check + commit previous phase ──
    if [[ $phase -ne $current_phase ]]; then
      if [[ $current_phase -ne 0 ]]; then
        local error_output check_result
        error_output=$(run_sanity_checks "$current_phase" 2>&1) && check_result=0 || check_result=$?

        if [[ $check_result -ne 0 ]]; then
          local fixed=false
          for attempt in $(seq 1 $MAX_FIX_ATTEMPTS); do
            fix_build "$current_phase" "$error_output" "$attempt"
            error_output=$(run_sanity_checks "$current_phase" 2>&1) && check_result=0 || check_result=$?
            if [[ $check_result -eq 0 ]]; then
              fixed=true
              log_success "  Build fixed on attempt ${attempt}"
              break
            fi
          done

          if [[ "$fixed" != "true" ]]; then
            log_error "  Phase ${current_phase} could not be fixed after ${MAX_FIX_ATTEMPTS} attempts."
            log_error "  Halting. Check ralph.log for details."
            exit 1
          fi
        fi

        commit_phase "$current_phase" "$(get_phase_name $current_phase)"
      fi

      current_phase=$phase
      log_phase "$phase — $(get_phase_name $phase)"
    fi

    # ── Skip completed tasks ──
    if is_task_done "$task_id"; then
      log "  Skipping ${task_id} (already done)"
      continue
    fi

    # ── Execute task ──
    log "${YELLOW}► Task ${task_id}: ${description}${NC}"
    run_task "$task_id" "$description"
    mark_task_done "$task_id"
  done

  # ── Final phase: sanity check + commit ──
  if [[ $current_phase -ne 0 ]]; then
    local error_output check_result
    error_output=$(run_sanity_checks "$current_phase" 2>&1) && check_result=0 || check_result=$?

    if [[ $check_result -ne 0 ]]; then
      local fixed=false
      for attempt in $(seq 1 $MAX_FIX_ATTEMPTS); do
        fix_build "$current_phase" "$error_output" "$attempt"
        error_output=$(run_sanity_checks "$current_phase" 2>&1) && check_result=0 || check_result=$?
        if [[ $check_result -eq 0 ]]; then
          fixed=true
          log_success "  Build fixed on attempt ${attempt}"
          break
        fi
      done

      if [[ "$fixed" != "true" ]]; then
        log_error "  Final phase could not be fixed after ${MAX_FIX_ATTEMPTS} attempts."
        log_error "  Halting. Check ralph.log for details."
        exit 1
      fi
    fi

    commit_phase "$current_phase" "$(get_phase_name $current_phase)"
  fi

  echo ""
  log_success "${BOLD}Ralph complete! All phases built and committed.${NC}"
  log "Check FINDINGS.md for decisions log."
  log "Check ralph.log for full output."
}

main "$@"
