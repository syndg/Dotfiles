#!/usr/bin/env zsh
# ralph.sh — Automated Claude Code builder
# Executes tasks from PRD.md one at a time in headless mode.
# Runs sanity checks between phases. Self-heals on build failures (max 3 attempts).
# Commits once per phase. Tracks findings in FINDINGS.md.

set -euo pipefail

# ── Trap Ctrl+C to exit cleanly ──────────────────────────
cleanup() {
  echo ""
  echo -e "\033[0;31m[$(date '+%Y-%m-%d %H:%M:%S')] Ralph interrupted. Exiting.\033[0m"
  kill 0 2>/dev/null
  exit 130
}
trap cleanup SIGINT SIGTERM

PROJECT_DIR="$(cd "$(dirname "$0")" && pwd)"
PRD_FILE="$PROJECT_DIR/PRD.md"
FINDINGS_FILE="$PROJECT_DIR/FINDINGS.md"
LOG_FILE="$PROJECT_DIR/ralph.log"
MAX_FIX_ATTEMPTS=3

# ── Colors ────────────────────────────────────────────────
RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
BLUE='\033[0;34m'
CYAN='\033[0;36m'
BOLD='\033[1m'
