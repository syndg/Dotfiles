run_task() {
  local task_id="$1"
  local description="$2"

  # Uncomment if using UI_TASKS:
  # local ui_instruction=""
  # if is_ui_task "$task_id"; then
  #   ui_instruction="
  # - IMPORTANT: Use the /frontend-design skill for this task."
  # fi

  local prompt="You are implementing a software project.

PROJECT DIRECTORY: $PROJECT_DIR

YOUR TASK: Execute task **${task_id}** — \"${description}\"

Read the full task specification from PRD.md (task ${task_id}) and implement it exactly.
Read FINDINGS.md to understand any decisions or context from previous tasks.

RULES:
- Only do what this specific task requires. Do not touch other tasks.
- After completing the task, append your findings/decisions to FINDINGS.md under a heading '## Task ${task_id}: ${description}'.
  Include: what you did, any decisions you made, any issues encountered, and any notes for future tasks. Keep it concise (3-8 bullet points).
- Do NOT commit anything to git.
- Do NOT run the dev server or any long-running processes.
- If a task says to install packages, actually run the install command.
- If a task says to create a file, create the file with the full implementation.
- Make sure all types and imports are correct."

  log "  Running Claude for task ${task_id}..."

  # ════════════════════════════════════════════════════════
  #  CUSTOMIZE: --model and --allowedTools
